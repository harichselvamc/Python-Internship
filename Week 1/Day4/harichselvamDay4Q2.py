month=["jan","Feb","Mar","Apr","May","jun","jul","Aug","Sep","Oct","Nov","Dec"]
#Append- Adds an object (a number, a string or a  another list) at the end of the given list
month.append("pluto")
print("after using append function",month)
#Extend- The length of the list increases by a number of elements in its argument.
month.extend("pluto")
print("after using extend function",month)