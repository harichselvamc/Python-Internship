#Indexing is used to obtain individual elements.
month="june"
print (month[0])
print (month[1])
print (month[2])
print (month[3])
print("then Negative indexing")
print (month[-1])
print (month[-2])
print (month[-3])
print (month[-4])
#Slicing is used to obtain a sequence of elements.
print("this is slicing")
print(month[0:4])
print(month[:4])
print(month[0:])
print(month[-4:])